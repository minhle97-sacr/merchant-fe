const minAdDate = new Date('1943-04-14');
const maxAdDate = new Date('2034-04-13');

const minBsYear = 2000;
const maxBsYear = 2090;

/**
 * Converts AD Date to BS Date string (YYYY-MM-DD)
 */
export function adToBs(date: Date): string | null {
  if (date < minAdDate || date > maxAdDate) {
    return null;
  }

  let totalDiff = Math.floor((date.getTime() - minAdDate.getTime()) / (1000 * 60 * 60 * 24));
  
  let bsYear = minBsYear;
  let bsMonth = 1;
  let bsDay = 1;

  // Find Year
  while (totalDiff >= totalDaysInBsYear(bsYear)) {
    totalDiff -= totalDaysInBsYear(bsYear);
    bsYear++;
  }

  // Find Month
  const months = calendarData[bsYear];
  for (let i = 0; i < 12; i++) {
    if (totalDiff < months[i]) {
      bsMonth = i + 1;
      bsDay = totalDiff + 1;
      break;
    }
    totalDiff -= months[i];
  }

  return `${bsYear}-${bsMonth.toString().padStart(2, '0')}-${bsDay.toString().padStart(2, '0')}`;
}

/**
 * Converts BS Date string (YYYY-MM-DD) to AD Date
 */
export function bsToAd(bsDateStr: string): Date | null {
  const parts = bsDateStr.split('-').map(Number);
  if (parts.length !== 3) return null;

  const [bsYear, bsMonth, bsDay] = parts;

  if (bsYear < minBsYear || bsYear > maxBsYear || bsMonth < 1 || bsMonth > 12) {
    return null;
  }

  const months = calendarData[bsYear];
  if (bsDay < 1 || bsDay > months[bsMonth - 1]) {
    return null;
  }

  let totalDiff = 0;

  // Add days for years
  for (let year = minBsYear; year < bsYear; year++) {
    totalDiff += totalDaysInBsYear(year);
  }

  // Add days for months in current year
  for (let month = 1; month < bsMonth; month++) {
    totalDiff += months[month - 1];
  }

  // Add days in current month
  totalDiff += bsDay - 1;

  const adDate = new Date(minAdDate);
  adDate.setDate(adDate.getDate() + totalDiff);

  return adDate;
}

function totalDaysInBsYear(year: number): number {
  return calendarData[year].reduce((acc, days) => acc + days, 0);
}
